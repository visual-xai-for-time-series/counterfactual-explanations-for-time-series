# TimeX++: Learning Time-Series Explanations with Information Bottleneck
#
# Paper: Liu, Z., Wang, T., Shi, J., Xu, L., Zhou, Q., Tripathi, P., Cai, X., & Hu, X. (2024).
#        "TimeX++: Learning Time-Series Explanations with Information Bottleneck"
#        Proceedings of the 41st International Conference on Machine Learning (ICML 2024)
#
# Paper URL: https://arxiv.org/abs/2405.09308
# GitHub: https://github.com/zichuan-liu/TimeXplusplus
#
# Improved time series explainer based on information bottleneck principle that generates
# in-distributed and label-preserving explanation instances. Addresses distribution shift
# issues and signaling problems in applying IB to time series explainability.
#
# Key Features:
# - Information bottleneck-based explanation framework
# - Explanation extractor with transformer encoder-decoder
# - Explanation conditioner for in-distribution instance generation  
# - Avoids out-of-distribution (OOD) issues
# - Label consistency through JS divergence
# - Produces compact, informative explanations
